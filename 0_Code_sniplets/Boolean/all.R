# Check if everything is TRUE or FALSE


# example numbers

x <- 3
y <- 1:10

a <- 1
b <- 1:10


# ask if all values are larger than the other ones

all(x > y)
all(a > b)

# ask what is the opposite of: ask if all values are larger than the other ones
# this is not the same like: ask if all value are smaller than the other ones

all(!x > y)
all(!a > b)

# ask if all value are smaller than the other ones

all(x < y)
all(a < b)







